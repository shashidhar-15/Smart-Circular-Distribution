// === OLED Display Functions ===
void OLED_Display_Char(unsigned char x, unsigned char y, char chr) { unsigned char i, index;
if (chr >= 'A' && chr <= 'Z') {
 
index = chr - 'A';
} else if (chr >= 'a' && chr <= 'z') { index = chr - 'a' + 26;
} else if (chr >= '0' && chr <= '9') { index = chr - '0' + 52;
} else {
return; // Unsupported character
}
OLED_Set_Pos(x, y); for (i = 0; i < 6; i++) {
OLED_Write_Data(font6x8_combined[index][i]);
}
}

void OLED_Display_String(unsigned char x, unsigned char y, char *str) { while (*str) {
OLED_Display_Char(x, y, *str); x += 6;
str++;
}
}

void OLED_Display_Number(unsigned char x, unsigned char y, unsigned int num) { char buf[6];
unsigned char i = 0, j; if (num == 0) {
buf[i++] = '0';
} else {
while (num > 0 && i < sizeof(buf)) { buf[i++] = (num % 10) + '0';
num /= 10;
}
}
for (j = 0; j < i; j++) {
OLED_Display_Char(x + j * 6, y, buf[i - 1 - j]);
}
}

